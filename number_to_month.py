def number_to_month(month):
    if month == 1:
        month = "enero"
    elif month == 2:
        month = "febrero"
    elif month == 3:
        month = "marzo"
    elif month == 4:
        month = "abril"
    elif month == 5:
        month = "mayo"
    elif month == 6:
        month = "junio"
    elif month == 7:
        month = "julio"
    elif month == 8:
        month = "agosto"
    elif month == 9:
        month = "septiembre"
    elif month == 10:
        month = "octubre"
    elif month == 11:
        month = "noviembre"
    elif month == 12:
        month = "diciembre"
    else:
        month = "error"
    return month

